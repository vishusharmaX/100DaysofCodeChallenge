class Solution {
  public:
    // Function to rotate a linked list.
    Node* rotate(Node* head, int k) {
        // Your code here
        vector<int>ans;
        while(head != NULL){
            ans.push_back(head->data);
            head = head->next;
        }
        
        if(k == 0) return head;
        int n = ans.size();
        k  = k % n;
        // k = k-1;
        reverse(ans.begin(),ans.begin()+k);
        reverse(ans.begin()+k,ans.end());
        reverse(ans.begin(),ans.end());
        
        for(auto it : ans){
            cout<<it<<" ";
        }
        
        return head;
        
    }
};


