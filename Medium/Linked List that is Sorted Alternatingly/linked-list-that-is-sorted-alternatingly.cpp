#include <vector>
#include <algorithm>

class Solution
{
public:
    void sort(Node **head)
    {
        vector<int> ans;
        Node *current = *head;
        while(current != NULL){
            ans.push_back(current->data);
            current = current->next;
        }
        
       std::sort(ans.begin(),ans.end());
        
        Node *dummyNode = new Node(-1);
        Node *temp = dummyNode;
        for(int i = 0; i < ans.size(); i++){
            Node *newNode = new Node(ans[i]);
            temp->next = newNode;
            temp = newNode;
        }
        
        *head = dummyNode->next;
    }
};



